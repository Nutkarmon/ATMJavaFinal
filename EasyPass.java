class EasyPass {
	private double inpute;
	private String inputenumber;

	EasyPass(){}

    EasyPass(double inpute,String inputenumber){
	this.inpute = inpute;
	this.inputenumber = inputenumber;
}
    public void setinpute(double inpute){
    this.inpute = inpute;
}
    public double getinpute(){
    return inpute;
}
    public void setinputenumber(String inputenumber){
    this.inputenumber = inputenumber;
}
    public String getinputenumber(){
    return inputenumber;
}

}
